class Utils:
    """Helper functions"""

    def read_params(self, config_path):
        with open(config_path) as yaml_file:
            config = yaml.safe_load(yaml_file)
        return config

    def get_data(self, data_path):
        df = pd.read_csv(data_path)
        return df


